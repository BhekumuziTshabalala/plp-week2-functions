int addTwo(int num1, int num2) {
    return num1 + num2;
  }

int subractTwo(int num1, int num2) {
    return num1 - num2;
  }


int multiplyTwo(int num1, int num2) {
    return num1 * num2;
  }

double divideTwo(int num1, int num2) {
    return num1 / num2;
  }

